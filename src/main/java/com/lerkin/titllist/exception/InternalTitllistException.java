package com.lerkin.titllist.exception;

public class InternalTitllistException extends RuntimeException {

	public InternalTitllistException() {

		super();
	}

	public InternalTitllistException(String message) {

		super(message);
	}

	public InternalTitllistException(String message, Throwable cause) {

		super(message, cause);
	}

	public InternalTitllistException(Throwable cause) {

		super(cause);
	}
}
